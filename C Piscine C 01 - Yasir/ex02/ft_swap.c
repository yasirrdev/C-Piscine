#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	swp;	

	swp = *a;
	*a = *b;
	*b = swp;
}

int main(void)
{
	int a = 10;
	int b = 5;

	ft_swap(&a, &b);
	printf("%d\n", b);
	return(0);
}
